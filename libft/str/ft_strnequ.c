#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 && s2 && n > 0 && (*s1 != '\0' || *s2 != '\0'))
		return (*s1 - *s2) ? 0 : ft_strnequ(s1 + 1, s2 + 1, n - 1);
	return (((s1 && !s2) || (!s1 && s2)) ? 0 : 1);
}
